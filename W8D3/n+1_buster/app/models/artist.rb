class Artist < ApplicationRecord
  has_many :albums,
    class_name: 'Album',
    foreign_key: :artist_id,
    primary_key: :id

  def n_plus_one_tracks
    albums = self.albums
    tracks_count = {}
    albums.each do |album|
      tracks_count[album.title] = album.tracks.length
    end

    tracks_count
  end

  def better_tracks_query
    # TODO: your code here
    self.albums
      .select("albums.title, COUNT(tracks.id) AS tracks_count")
      .joins(:tracks)
      .where("albums.artist_id = $1") 
      .group("tracks.album_id, albums.title").as_json.map(&:compact)
  end
end
